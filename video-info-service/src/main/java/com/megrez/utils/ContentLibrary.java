    /**
     * 获取整个内容映射表。
     *
     * @return 不可变的Map，包含所有分类和内容列表
     */
    public static Map<String, List<String>> getContentMap() {
        // 返回不可变视图，防止外部修改
        return Collections.unmodifiableMap(CONTENT_MAP);
    }

    /**
     * 随机返回一个分类及其对应列表中的随机一条内容。
     * 返回的Map中只包含一对键值对：随机选择的分类 -> 随机选择的一条内容（封装为单元素列表）。
     *
     * @return 包含随机分类和随机内容的Map，如果数据为空则返回空Map
     */
    public static Map<String, List<String>> getRandomCategoryAndContent() {
        if (CONTENT_MAP.isEmpty()) {
            return Collections.emptyMap();
        }

        // 1. 随机选择一个分类键
        List<String> keys = new ArrayList<>(CONTENT_MAP.keySet());
        String randomKey = keys.get(new Random().nextInt(keys.size()));

        // 2. 获取该分类对应的内容列表
        List<String> contentList = CONTENT_MAP.get(randomKey);
        if (contentList == null || contentList.isEmpty()) {
            return Collections.emptyMap();
        }

        // 3. 从列表中随机选择一条内容
        String randomContent = contentList.get(new Random().nextInt(contentList.size()));

        // 4. 构建返回结果
        Map<String, List<String>> result = new HashMap<>();
        result.put(randomKey, Collections.singletonList(randomContent));

        return result;
    }

    /**
     * 随机返回一个分类及其对应列表中的随机一条内容（简化版本）。
     * 返回一个Map.Entry，包含分类和随机内容。
     *
     * @return Map.Entry，键为分类，值为随机内容；如果数据为空则返回null
     */
    public static Map.Entry<String, String> getRandomEntry() {
        if (CONTENT_MAP.isEmpty()) {
            return null;
        }

        List<String> keys = new ArrayList<>(CONTENT_MAP.keySet());
        String randomKey = keys.get(new Random().nextInt(keys.size()));

        List<String> contentList = CONTENT_MAP.get(randomKey);
        if (contentList == null || contentList.isEmpty()) {
            return null;
        }

        String randomContent = contentList.get(new Random().nextInt(contentList.size()));
        return new AbstractMap.SimpleEntry<>(randomKey, randomContent);
    }

